var somaESubtracao = 10 + 5 - 3;
var subtracaoEMultiplicacao = 10 - 5 * 3;
var multiplicacaoEExponenciacao = 10 * 5 ** 2;
var agrupamentoEDivisao = (10 + 5) / 3;

console.log(somaESubtracao); // 12
console.log(subtracaoEMultiplicacao); // -5
console.log(multiplicacaoEExponenciacao); // 250
console.log(agrupamentoEDivisao); // 5

